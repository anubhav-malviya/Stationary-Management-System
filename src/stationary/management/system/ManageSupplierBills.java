    public void validateFields() {
        String billNo = txtBillNo.getText();
        String supplierName = txtSupplierName.getText();
        String dateOfPurchase = txtDateOfPurchase.getText();
        String amount = txtAmount.getText();
        String billPaid = (String) txtBillPaid.getSelectedItem();

        if (!billNo.equals("") && !supplierName.equals("") && !dateOfPurchase.equals("") && !amount.equals("") && billPaid != null) {
            btnUpdate.setEnabled(true);
        } else {
            btnUpdate.setEnabled(false);
        }
    }

    public void tableDetails() {
        String status = (String) jComboBox1.getSelectedItem();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Supplier> list;
        if (status.equals("All")) {
            list = SupplierDao.getAllRecords();
        } else {
            list = SupplierDao.getAllRecords(status);
        }
        Iterator<Supplier> itr = list.iterator();
        while (itr.hasNext()) {
            Supplier supplierObj = itr.next();
            dtm.addRow(new Object[]{supplierObj.getId(), supplierObj.getBillNo(), supplierObj.getSupplierName(), supplierObj.getDate(), supplierObj.getAmount(), supplierObj.getBillPaid()});
        }
    }

