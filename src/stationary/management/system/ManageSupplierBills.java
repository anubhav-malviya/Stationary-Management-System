    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        lblId.setText(id);
        String billNo = model.getValueAt(index, 1).toString();
        txtBillNo.setText(billNo);
        String supplierName = model.getValueAt(index, 2).toString();
        txtSupplierName.setText(supplierName);
        String date = model.getValueAt(index, 3).toString();
        txtDateOfPurchase.setText(date);
        String amount = model.getValueAt(index, 4).toString();
        txtAmount.setText(amount);
        String billPaid = model.getValueAt(index, 5).toString();
        txtBillPaid.removeAllItems();
        txtBillPaid.addItem(billPaid);
        if (billPaid.equals("Yes")) {
            txtBillPaid.addItem("No");
        } else {
            txtBillPaid.addItem("Yes");
        }

        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id = lblId.getText();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Product", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            SupplierDao.delete(Integer.parseInt(id));
            setVisible(false);
            new ManageSupplierBills().setVisible(true);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

